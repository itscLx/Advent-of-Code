public func day3(){
    // PART 1
    let lines = input.split(separator: "\n").map{Array(String($0))}
    var solution = downTheSloap(lines: lines, right: 1, down: 1)
    solution *= downTheSloap(lines: lines, right: 3, down: 1)
    solution *= downTheSloap(lines: lines, right: 5, down: 1)
    solution *= downTheSloap(lines: lines, right: 7, down: 1)
    solution *= downTheSloap(lines: lines, right: 1, down: 2)
    
    print(solution)
}


func downTheSloap(lines: [[String.Element]], right: Int, down: Int) -> Int {
    var x = 0
    var trees = 0
 
    for i in stride(from: down, to: lines.count, by: down){
        let line = lines[i]
        x += right
        if line[(x % line.count + line.count) % line.count] == "#" { trees += 1}
    }
    
    print(trees)
    return trees
}

